/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* Enable I-Cache---------------------------------------------------------*/
  SCB_EnableICache();

  /* Enable D-Cache---------------------------------------------------------*/
  SCB_EnableDCache();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  I2C_SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USB_OTG_FS_PCD_Init();
  BSP_LED_Init(LED1);
  BSP_LED_Init(LED2);
  BSP_LED_Init(LED3);

  comms_init();
  size_t ccount = 0;
  send_sync((uint8_t*)"\r\n$ ", 4);

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  uint8_t buf[256];
  uint8_t* rptr = buf;
  while (1) {
    recv_sync(rptr, 1);
    if (*rptr == 27) {
      // Capture esc character
      *rptr = '^';
    } else if (*rptr == '\n') {
      // Ignore newline
      continue;
    }
    if (*rptr == '\r' || (rptr - buf) == 255) {
      *rptr = '\0';
      fprintf(stderr, "%d Got command: %s\r\n", ccount++, (char*)buf);
      processData((char*)buf);
      send_sync((uint8_t*)"$ ", 2);
      rptr = buf;
    } else {
      ++rptr;
    }
  }
}


